export function sumar(num1, num2) {
    return num1 + num2;
}
export function restar(num1, num2) {
    return num1 - num2;
}
export function multiplicar(num1, num2) {
    return num1 * num2;
}
function dividir(num1, num2) {
    return num1 / num2;
}
function modulo(num1, num2) {
    return num1 % num2;
}
function potencia(num1, num2) {
    return num1**num2;
}


